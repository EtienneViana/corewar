#include "libft.h"

size_t	ft_cntdigit(int n)
{
	size_t i;

	i = 1;
	if (n == -2147483648)
	{
		i += 2;
		n = 147483648;
	}
	if (n < 0)
	{
		n = -n;
		i++;
	}
	if (n / 10)
		i += ft_cntdigit(n / 10);
	return (i);
}
